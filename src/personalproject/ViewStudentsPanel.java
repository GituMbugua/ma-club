    public void showUsers() {
        ArrayList<TKStudent> tkList = getTkList();
        ArrayList<KarStudent> karList = getKarList();
        DefaultTableModel model = (DefaultTableModel)tblDisplayStudents.getModel();
        
        model.setRowCount(0);
        Object[] col = new Object[12];
        for (int i=0; i<tkList.size(); i++) {
            col[0] = tkList.get(i).getFname();
            col[1] = tkList.get(i).getSname();
            col[2] = tkList.get(i).getGender();
            col[3] = tkList.get(i).getAge();
            col[4] = tkList.get(i).getNumber();
            col[5] = tkList.get(i).getCourse();
            col[6] = tkList.get(i).getYear();
            col[7] = tkList.get(i).getGeup();
            col[8] = tkList.get(i).getBelt();
            col[9] = karList.get(i).getStyle();
            col[10] = karList.get(i).getKyu();
            col[11] = karList.get(i).getBelt();
            model.addRow(col);
        }
    }
