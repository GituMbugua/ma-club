    public void myConnection() {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            String database = "jdbc:mysql://localhost:3306/ma_club";
            con = DriverManager.getConnection(database, "root", "");
            
        }
        catch (Exception ex) {
            System.out.println("Connection error! " + ex);
        }
    }
    
    public ArrayList<TKStudent> getTkList() {
       ArrayList<TKStudent> tkStudentsList = new ArrayList();
       try {
           con.close();
           myConnection();
           
           String query = "SELECT * FROM students";
           st = con.createStatement();
           rs = st.executeQuery(query);
           
           TKStudent student;
           while (rs.next()) {
               student = new TKStudent(rs.getString("fname"), rs.getString("sname"), rs.getString("gender"), 
                       rs.getInt("age"), rs.getString("phone"), rs.getString("course"), rs.getString("year"),
                       rs.getString("geup"), rs.getString("tkbelt"));
               tkStudentsList.add(student);
           }
        }
       catch (Exception ex) {
            System.out.println("Error! " + ex);
        }
       return tkStudentsList;
    }
    
